#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	while (n-- > 0)
	{
		if (d != s)
			*d = *s;
		d++;
		s++;
	}
	return (dst);
}
